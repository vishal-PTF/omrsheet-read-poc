def rectContour(contours):

    rectCon = []
    max_area = 0
    for i in contours:
        area = cv2.contourArea(i)
        if area > 50:
            peri = cv2.arcLength(i, True)
            approx = cv2.approxPolyDP(i, 0.02 * peri, True)
            if len(approx) == 4:
                rectCon.append(i)
    rectCon = sorted(rectCon, key=cv2.contourArea,reverse=True)
    #print(len(rectCon))
    return rectCon
def getCornerPoints(cont):
    peri = cv2.arcLength(cont, True) # LENGTH OF CONTOUR
    approx = cv2.approxPolyDP(cont, 0.02 * peri, True) # APPROXIMATE THE POLY TO GET CORNER POINTS
    return approx


def circleContour(contours):
    circleCon = []
    for contour in contours:
        area = cv2.contourArea(contour)
        if area > 50:
            perimeter = cv2.arcLength(contour, True)
            if perimeter == 0:  # To avoid division by zero
                continue
            circularity = 4 * np.pi * (area / (perimeter * perimeter))
            if 0.7 < circularity < 1.2:  # Adjust the range to match circular contours more precisely
                ((x, y), radius) = cv2.minEnclosingCircle(contour)
                center = (int(x), int(y))
                radius = int(radius)
                circleCon.append((center, radius))
    return circleCon





def is_circle_filled(image, center, radius):
    mask = np.zeros_like(image, dtype=np.uint8)
    cv2.circle(mask, center, radius, 255, -1)  # Draw a filled circle on the mask

    # Count the number of white pixels inside the circle
    circle_area = np.pi * (radius ** 2)
    filled_pixels = cv2.countNonZero(cv2.bitwise_and(image, image, mask=mask))
    fill_ratio = filled_pixels / circle_area
    
    # Consider the circle filled if the fill ratio is above a certain threshold
    threshold = 0.3  # Adjust threshold as necessary
    is_filled = fill_ratio > threshold
    
    return is_filled, fill_ratio